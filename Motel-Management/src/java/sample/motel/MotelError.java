package sample.motel;

/**
 *
 * @author Bao
 */
public class MotelError {
    private String motelIDError;
    private String nameError;
    private String imageError;
    private String phoneError;
    private String DesctError;
    private String addressError;
    private String districtError;
    private String cityError;
    private String ratingError;
    private String ownerIdError;
    private String statusError;

    public MotelError() {
    }

    public MotelError(String motelIDError, String nameError, String imageError, String phoneError, String DesctError, String addressError, String districtError, String cityError, String ratingError, String ownerIdError, String statusError) {
        this.motelIDError = motelIDError;
        this.nameError = nameError;
        this.imageError = imageError;
        this.phoneError = phoneError;
        this.DesctError = DesctError;
        this.addressError = addressError;
        this.districtError = districtError;
        this.cityError = cityError;
        this.ratingError = ratingError;
        this.ownerIdError = ownerIdError;
        this.statusError = statusError;
    }

    public String getMotelIDError() {
        return motelIDError;
    }

    public void setMotelIDError(String motelIDError) {
        this.motelIDError = motelIDError;
    }

    public String getNameError() {
        return nameError;
    }

    public void setNameError(String nameError) {
        this.nameError = nameError;
    }

    public String getImageError() {
        return imageError;
    }

    public void setImageError(String imageError) {
        this.imageError = imageError;
    }

    public String getPhoneError() {
        return phoneError;
    }

    public void setPhoneError(String phoneError) {
        this.phoneError = phoneError;
    }

    public String getDesctError() {
        return DesctError;
    }

    public void setDesctError(String DesctError) {
        this.DesctError = DesctError;
    }

    public String getAddressError() {
        return addressError;
    }

    public void setAddressError(String addressError) {
        this.addressError = addressError;
    }

    public String getDistrictError() {
        return districtError;
    }

    public void setDistrictError(String districtError) {
        this.districtError = districtError;
    }

    public String getCityError() {
        return cityError;
    }

    public void setCityError(String cityError) {
        this.cityError = cityError;
    }

    public String getRatingError() {
        return ratingError;
    }

    public void setRatingError(String ratingError) {
        this.ratingError = ratingError;
    }

    public String getOwnerIdError() {
        return ownerIdError;
    }

    public void setOwnerIdError(String ownerIdError) {
        this.ownerIdError = ownerIdError;
    }

    public String getStatusError() {
        return statusError;
    }

    public void setStatusError(String statusError) {
        this.statusError = statusError;
    }
    
    
}
